void	ft_putchar(char c);

int		main(int c, char **v)
{
	int i;
	int	j;

	(void)c;
	i = 1;
	while (v[i])
	{
		j = 0;
		while (v[i][j])
		{
			ft_putchar(v[i][j]);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
