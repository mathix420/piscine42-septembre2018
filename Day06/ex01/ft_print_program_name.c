void	ft_putchar(char c);

int		main(int c, char **v)
{
	int	i;

	(void)c;
	i = 0;
	while (v[0][i])
	{
		ft_putchar(v[0][i]);
		i++;
	}
	ft_putchar('\n');
	return (0);
}
