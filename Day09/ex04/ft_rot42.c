char	ft_rot_char(char c)
{
	if (c >= 'a' && c <= 'z')
		c = (c - 'a' + 42) % 26 + 'a';
	else if (c >= 'A' && c <= 'Z')
		c = (c - 'A' + 42) % 26 + 'A';
	else if (c >= '0' && c <= '9')
		c = (c - '0' + 42) % 10 + '0';
	return (c);
}

char	*ft_rot42(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
	{
		str[i] = rot(str[i]);
		i++;
	}
	return (str);
}
