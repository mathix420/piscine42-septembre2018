char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	char	c;

	n = 0;
	i = 0;
	while (str[i])
	{
		i++;
	}
	i--;
	while (i > n)
	{
		c = str[i];
		str[i] = str[n];
		str[n] = c;
		n++;
		i--;
	}
	return (str);
}
