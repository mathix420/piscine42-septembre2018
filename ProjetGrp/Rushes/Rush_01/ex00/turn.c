int		ft_turn_table(int **tab)
{
	int		i;
	int		j;
	int		*line;

	i = 0;
	j = 8;
	while (i < j)
	{
		line = tab[i];
		tab[i] = tab[j];
		tab[j] = line;
		j--;
		i++;
	}
	return (0);
}
