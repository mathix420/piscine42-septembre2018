int		ft_sqrt(int nb)
{
	int	i;
	int b;

	i = 0;
	b = i * i;
	if (nb < 1)
		return (0);
	while (i <= nb / 2)
	{
		if (b == nb)
			return (i);
		i++;
		b = i * i;
	}
	return (0);
}
