int		match(char *s1, char *s2)
{
	int		t_1;
	int		t_2;

	if (*s1 != '\0' && *s2 == '*')
	{
		t_1 = match(s1, s2 + 1);
		t_2 = match(s1 + 1, s2);
		return ((t_1 + t_2) > 0);
	}
	if (*s1 == '\0' && *s2 == '*')
		return (match(s1, s2 + 1));
	if (*s1 != '\0' && *s2 != '\0' && *s1 == *s2)
		return (match(s1 + 1, s2 + 1));
	if (*s1 == '\0' && *s2 == '\0')
		return (1);
	return (0);
}
